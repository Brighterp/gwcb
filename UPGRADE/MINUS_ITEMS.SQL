SELECT  TRANSACTION_DATE, BRANCH_ID, CURRENT_BALANCE, (SELECT PRIMARY_NAME
    FROM DEPARTMENTS WHERE DEPARTMENT_ID = STORES_ID) STORE_NAME, ITEMS.USER_CODE ITEM_CODE , BASE_DOCUMENT_NO , BASE_DOCUMENT_TYPES_ID 
FROM ITEMS_TRANSACTIONS , ITEMS
WHERE   ITEMS.ITEMS_ID = ITEMS_TRANSACTIONS.ITEMS_ID
AND CURRENT_BALANCE < 0
order by BRANCH_ID, TRANSACTION_DATE
/



 SELECT  DISTINCT ITEMS.USER_CODE ITEM_CODE, (SELECT PRIMARY_NAME
     FROM DEPARTMENTS WHERE DEPARTMENT_ID = STORES_ID) STORE_NAME, BRANCH_ID
  FROM ITEMS_TRANSACTIONS , ITEMS
  WHERE   ITEMS.ITEMS_ID = ITEMS_TRANSACTIONS.ITEMS_ID
  AND CURRENT_BALANCE < 0
 order by BRANCH_ID
/



SELECT DISTINCT ITEMS.USER_CODE,ITEMS.PRIMARY_NAME ,DEPARTMENTS.PRIMARY_NAME
FROM ITEMS,ITEMS_TRANSACTIONS ,DEPARTMENTS
WHERE ITEMS.ITEMS_ID=ITEMS_TRANSACTIONS.ITEMS_ID 
AND DEPARTMENTS.DEPARTMENT_ID=ITEMS_TRANSACTIONS.STORES_ID
AND (ITEMS_TRANSACTIONS.CURRENT_BALANCE <0  OR ITEMS_TRANSACTIONS.AVERAGE_COST < 0);

