ALTER TABLE SAL_COUPON_BOOK ADD(COUPON_SAL_PRICE NUMBER(17,5) DEFAULT 0 NOT NULL);

CREATE OR REPLACE VIEW SAL_COUPON_VIEW
(SAL_COUPON_BOOK_ID, BRANCH_ID, ISSUE_DATE, VALID_UNTIL_DATE, FROM_NO, 
 TO_NO, COUPON_VALUE, SOLD_FLAG, SAL_COUPON_NO, USED_FLAG, 
 USED_DATE, COUPON_SAL_PRICE)
AS 
select
   ALL SAL_COUPON_BOOK.SAL_COUPON_BOOK_ID,
   SAL_COUPON_BOOK.BRANCH_ID,
   SAL_COUPON_BOOK.ISSUE_DATE,
   SAL_COUPON_BOOK.VALID_UNTIL_DATE,
   SAL_COUPON_BOOK.FROM_NO,
   SAL_COUPON_BOOK.TO_NO,
   SAL_COUPON_BOOK.COUPON_VALUE,
   SAL_COUPON_BOOK.SOLD_FLAG,
   SAL_COUPON.SAL_COUPON_NO,
   SAL_COUPON.USED_FLAG,
   SAL_COUPON.USED_DATE,
   SAL_COUPON_BOOK.COUPON_SAL_PRICE
from
   SAL_COUPON_BOOK,
   SAL_COUPON
where
   SAL_COUPON_BOOK.SAL_COUPON_BOOK_ID = SAL_COUPON.SAL_COUPON_BOOK_ID
/

